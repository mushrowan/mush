/// unique model identifier (e.g. "claude-sonnet-4-20250514")
#[derive(
    Debug, Clone, PartialEq, Eq, Hash, Display, From, Deref, AsRef, Serialize, Deserialize,
)]
pub struct ModelId(String);

impl From<&str> for ModelId {
    fn from(s: &str) -> Self {
        Self(s.to_string())
    }
}

impl ModelId {
    /// short form for status bar display
    ///
    /// abbreviates alphabetic segments to first letter, keeps numbers as-is:
    /// `claude-opus-4-6` → `c-o-4-6`, `gpt-5.3-codex` → `g-5.3-c`
    pub fn abbreviated(&self) -> String {
        fn abbreviate_part(s: &str) -> String {
            s.split('-')
                .map(|seg| {
                    if seg.chars().all(|c| c.is_ascii_alphabetic()) {
                        seg[..1].to_string()
                    } else {
                        seg.to_string()
                    }
                })
                .collect::<Vec<_>>()
                .join("-")
        }

        if let Some((prefix, rest)) = self.0.split_once('/') {
            format!("{}/{}", abbreviate_part(prefix), abbreviate_part(rest))
        } else {
            abbreviate_part(&self.0)
        }
    }
}

