/// millisecond unix timestamp
#[derive(
    Debug,
    Clone,
    Copy,
    Default,
    PartialEq,
    Eq,
    PartialOrd,
    Ord,
    Hash,
    Display,
    Serialize,
    Deserialize,
)]
pub struct Timestamp(u64);

impl Timestamp {
    pub const fn zero() -> Self {
        Self(0)
    }

    pub const fn from_ms(ms: u64) -> Self {
        Self(ms)
    }

    pub fn now() -> Self {
        Self(
            std::time::SystemTime::now()
                .duration_since(std::time::UNIX_EPOCH)
                .unwrap_or_default()
                .as_millis() as u64,
        )
    }

    pub fn as_ms(&self) -> u64 {
        self.0
    }

    /// format as a human-readable relative age (e.g. "30s", "5m", "2h")
    #[must_use]
    pub fn age_display(&self) -> String {
        let now = Self::now().as_ms();
        let then = self.0;
        if now <= then {
            return "now".into();
        }
        let secs = (now - then) / 1000;
        if secs < 60 {
            return format!("{secs}s");
        }
        let mins = secs / 60;
        if mins < 60 {
            return format!("{mins}m");
        }
        let hours = mins / 60;
        if hours < 24 {
            return format!("{hours}h");
        }
        let days = hours / 24;
        if days < 7 {
            return format!("{days}d");
        }
        let weeks = days / 7;
        if days < 30 {
            return format!("{weeks}w");
        }
        let months = days / 30;
        if days < 365 {
            return format!("{months}mo");
        }
        let years = days / 365;
        format!("{years}y")
    }
}

/// token count used in context windows, usage stats, and compaction thresholds.
/// wraps u64 to prevent mixing with arbitrary integers
#[derive(
    Debug,
    Clone,
    Copy,
    Default,
    PartialEq,
    Eq,
    PartialOrd,
    Ord,
    Hash,
    Display,
    Serialize,
    Deserialize,
)]
pub struct TokenCount(u64);

impl TokenCount {
    pub const ZERO: Self = Self(0);

    pub const fn new(n: u64) -> Self {
        Self(n)
    }

    pub const fn get(self) -> u64 {
        self.0
    }

    /// percentage of a context window this count represents
    pub fn percent_of(self, window: Self) -> f64 {
        if window.0 == 0 {
            return 0.0;
        }
        (self.0 as f64 / window.0 as f64) * 100.0
    }

    /// whether this count exceeds a fraction of a context window
    pub fn exceeds_fraction(self, window: Self, numerator: u64, denominator: u64) -> bool {
        self.0 * denominator > window.0 * numerator
    }
}

impl From<u64> for TokenCount {
    fn from(n: u64) -> Self {
        Self(n)
    }
}

impl From<usize> for TokenCount {
    fn from(n: usize) -> Self {
        Self(n as u64)
    }
}

impl std::ops::Add for TokenCount {
    type Output = Self;
    fn add(self, rhs: Self) -> Self {
        Self(self.0 + rhs.0)
    }
}

impl std::ops::AddAssign for TokenCount {
    fn add_assign(&mut self, rhs: Self) {
        self.0 += rhs.0;
    }
}

impl std::ops::Sub for TokenCount {
    type Output = Self;
    fn sub(self, rhs: Self) -> Self {
        Self(self.0.saturating_sub(rhs.0))
    }
}

impl std::ops::SubAssign for TokenCount {
    fn sub_assign(&mut self, rhs: Self) {
        self.0 = self.0.saturating_sub(rhs.0);
    }
}

